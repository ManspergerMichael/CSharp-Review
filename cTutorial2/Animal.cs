namespace cTutorial2
{
    class Animal
    {
        public double height {get; set;}
        public double weight {get; set;}
        public string sound {get; set;}
        public double name {get; set;}

        public Animal()
        {
            this.height = 0;
            this.weight = 0;
            this.sound = "No sound";
            this.sound = "No name";
        }
    }
}